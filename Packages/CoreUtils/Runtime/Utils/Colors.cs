		public static Color SetAlpha(this Color baseColor, float alpha) {
			Color color = baseColor;
			color.a = alpha;
			return color;
		}

		public static Color Faded(this Color baseColor, float alphaDelta) {
			Color color = baseColor;
			color.a = Mathf.Clamp01(color.a-alphaDelta);
			return color;
		}

		public static Color Saturated(this Color baseColor) {
			ColorHSL colorHSL = baseColor;
			colorHSL.s = 1.0f;
			return colorHSL;
		}

		public static Color Desaturated(this Color baseColor) {
			ColorHSL colorHSL = baseColor;
			colorHSL.s = 0.0f;
			return colorHSL;
		}


		public static Color Saturated(this Color baseColor,float saturationDelta) {
			ColorHSL colorHSL = baseColor;
			colorHSL.s = Mathf.Clamp01(colorHSL.s + saturationDelta);
			return colorHSL;
		}

		public static Color Desaturated(this Color baseColor,float saturationDelta) {
			ColorHSL colorHSL = baseColor;
			colorHSL.s = Mathf.Clamp01(colorHSL.s - saturationDelta);
			return colorHSL;
		}

		public static Color HueShifted(this Color baseColor,float hueDelta) {
			ColorHSL colorHSL = baseColor;
			colorHSL.h = Mathf.Repeat(colorHSL.h+hueDelta,360.0f);
			return colorHSL;
		}


		public static Color SetHue(this Color baseColor,float hue) {
			ColorHSL colorHSL = baseColor;
			colorHSL.h = Mathf.Repeat(hue,360.0f);
			return colorHSL;
		}

		public static Color SetHue(this Color baseColor,Color hueColor) {
			ColorHSL colorHSLBase = baseColor;
			ColorHSL colorHSLHue = hueColor;
			colorHSLBase.h = colorHSLHue.h;
			return colorHSLBase;
		}


		public static Color Lightened(this Color baseColor) {
			ColorHSL colorHSL = baseColor;
			colorHSL.l = 1.0f;
			return colorHSL;
		}

		public static Color Darkened(this Color baseColor) {
			ColorHSL colorHSL = baseColor;
			colorHSL.l = 0.0f;
			return colorHSL;
		}


		public static Color Lightened(this Color baseColor,float lightnessDelta) {
			ColorHSL colorHSL = baseColor;
			colorHSL.l = Mathf.Clamp01(colorHSL.l + lightnessDelta);
			return colorHSL;
		}

		public static Color Darkened(this Color baseColor,float lightnessDelta) {
			ColorHSL colorHSL = baseColor;
			colorHSL.l = Mathf.Clamp01(colorHSL.l - lightnessDelta);
			return colorHSL;
		}

		public static Color Modified(this Color baseColor,float hueDelta=0.0f, float saturationDelta=0.0f, float lightnessDelta=0.0f,float alphaDelta=0.0f) {
			ColorHSL colorHSL = baseColor;
			colorHSL.h = Mathf.Repeat(colorHSL.h + hueDelta,360.0f);
			colorHSL.s = Mathf.Clamp01(colorHSL.s + saturationDelta);
			colorHSL.l = Mathf.Clamp01(colorHSL.l + lightnessDelta);
			colorHSL.a = Mathf.Clamp01(colorHSL.a + alphaDelta);
			return colorHSL;
		}

		public static Color MultiplyLuminance(this Color baseColor, float amount) {
			ColorHSL colorHSL = baseColor;
			float luminance = Mathf.GammaToLinearSpace(colorHSL.l);
			luminance *= amount;
			colorHSL.l = Mathf.LinearToGammaSpace(luminance);
			return colorHSL;
		}

